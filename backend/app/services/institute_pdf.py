"""A4 PDF for the AIIMS / All-India / Deemed modules.

Column layout adapts to the module:
  - AIIMS:      Sr, Institute Name, State Name, AIR, Score
  - All-India:  Sr, Institute Name, Degree, State Name, AIR, Score
  - Deemed:     Sr, Institute Name, Degree, State Name, AIR, Score
"""
from datetime import datetime, timezone
from io import BytesIO
from typing import List, Optional

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib.units import mm
from reportlab.platypus import Paragraph, SimpleDocTemplate, Spacer, Table, TableStyle

PRIMARY = colors.HexColor("#1e3a8a")
LIGHT = colors.HexColor("#eff6ff")
BAND_COLORS = {
    "High": colors.HexColor("#16a34a"),
    "Moderate": colors.HexColor("#d97706"),
    "Low": colors.HexColor("#dc2626"),
}

MODULE_TITLES = {
    "aiims": "AIIMS",
    "all-india": "All India (15%)",
    "deemed": "Deemed",
    "veterinary": "Veterinary",
}


def _footer(canvas, doc, brand: str | None = None):
    canvas.saveState()
    canvas.setFont("Helvetica", 8)
    canvas.setFillColor(colors.grey)
    now = datetime.now(timezone.utc).astimezone().strftime("%Y-%m-%d %H:%M")
    canvas.drawString(15 * mm, 10 * mm, f"Generated via {brand}" if brand else "Generated by NEET Medical College Predictor")
    canvas.drawCentredString(A4[0] / 2, 10 * mm, now)
    canvas.drawRightString(A4[0] - 15 * mm, 10 * mm, f"Page {doc.page}")
    canvas.setStrokeColor(colors.lightgrey)
    canvas.line(15 * mm, 13 * mm, A4[0] - 15 * mm, 13 * mm)
    canvas.restoreState()


def build_institute_pdf(
    *,
    module: str,
    student_name: str,
    mode: str,
    score: Optional[float],
    air: Optional[int],
    show_degree: bool,
    show_category: bool,
    results: List[dict],
    brand_headline: Optional[str] = None,
    letterhead: Optional[dict] = None,
) -> bytes:
    buf = BytesIO()
    module_title = MODULE_TITLES.get(module, module.title())
    top_margin = 18 * mm
    bottom_margin = 20 * mm
    if letterhead:
        top_margin = (10 + float(letterhead.get("header_h_mm", 0))) * mm
        bottom_margin = (12 + float(letterhead.get("footer_h_mm", 0))) * mm
    doc = SimpleDocTemplate(
        buf, pagesize=A4,
        leftMargin=15 * mm, rightMargin=15 * mm, topMargin=top_margin, bottomMargin=bottom_margin,
        title=f"{module_title} Prediction - {student_name}",
    )
    styles = getSampleStyleSheet()
    title = ParagraphStyle("t", parent=styles["Title"], textColor=PRIMARY, fontSize=18)
    sub = ParagraphStyle("s", parent=styles["Normal"], fontSize=9, textColor=colors.grey)
    cell = ParagraphStyle("c", fontSize=7, leading=8)

    story = [
        Paragraph(brand_headline or f"{module_title} Medical College Prediction", title),
        Spacer(1, 4),
    ]
    if brand_headline:
        story.append(Paragraph(f"{module_title} Prediction Report", sub))
        story.append(Spacer(1, 2))
    story += [
        Paragraph(
            f"Generated on {datetime.now(timezone.utc).astimezone().strftime('%d %b %Y, %H:%M')}",
            sub,
        ),
        Spacer(1, 10),
    ]

    if module == "veterinary":
        input_label = "Marks" if mode == "score" else "Rank"
    else:
        input_label = "NEET Score" if mode == "score" else "AIR"
    input_value = f"{score:g}" if mode == "score" else f"{air:,}"
    meta = [["Student Name", student_name, input_label, input_value]]
    meta_tbl = Table(meta, colWidths=[35 * mm, 70 * mm, 30 * mm, 45 * mm])
    meta_tbl.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (0, -1), LIGHT),
        ("BACKGROUND", (2, 0), (2, -1), LIGHT),
        ("TEXTCOLOR", (0, 0), (0, -1), PRIMARY),
        ("TEXTCOLOR", (2, 0), (2, -1), PRIMARY),
        ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
        ("FONTNAME", (2, 0), (2, -1), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, -1), 9),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
    ]))
    story.append(meta_tbl)
    story.append(Spacer(1, 14))

    # Veterinary uses its own fixed layout: Institute, State, Course, Marks, Rank
    # sorted by Rank (best first), per the requested PDF format.
    if module == "veterinary":
        vet_rows = sorted(results, key=lambda r: (r.get("air") is None, r.get("air") or 0))
        headers = ["Sr", "Institute Name", "State", "Course", "Marks", "Rank"]
        data = [headers]
        for i, r in enumerate(vet_rows, start=1):
            data.append([
                str(i),
                Paragraph(str(r["institute_name"]).title(), cell),
                Paragraph(str(r["state_name"]).title(), cell),
                Paragraph(str(r.get("degree") or "-"), cell),
                str(r["score"]),
                f"{r['air']:,}",
            ])
        widths = [9 * mm, 68 * mm, 28 * mm, 30 * mm, 17 * mm, 22 * mm]
        tbl = Table(data, colWidths=widths, repeatRows=1)
        tbl.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), PRIMARY),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, -1), 7),
            ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#cbd5e1")),
            ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, LIGHT]),
            ("TOPPADDING", (0, 0), (-1, -1), 3),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
        ]))
        story.append(tbl)
        if letterhead:
            from app.services.letterhead import make_letterhead_canvas
            doc.build(story, canvasmaker=make_letterhead_canvas(letterhead))
        else:
            def _pgv(canvas, doc_):
                _footer(canvas, doc_, brand_headline)

            doc.build(story, onFirstPage=_pgv, onLaterPages=_pgv)
        return buf.getvalue()

    # Build headers dynamically.
    headers = ["Sr", "Institute Name"]
    if show_degree:
        headers.append("Degree")
    headers.append("State Name")
    if show_category:
        headers.append("Category")
    headers += ["AIR", "Score", "Chance"]

    data = [headers]
    for r in results:
        row = [str(r["sr_no"]), Paragraph(str(r["institute_name"]).title(), cell)]
        if show_degree:
            row.append(r.get("degree") or "-")
        row.append(str(r["state_name"]).title())
        if show_category:
            row.append(r.get("category") or "-")
        row += [f"{r['air']:,}", str(r["score"]), r["chance"]]
        data.append(row)

    # Column widths.
    widths = [8 * mm, 62 * mm]
    if show_degree:
        widths.append(16 * mm)
    widths.append(34 * mm)
    if show_category:
        widths.append(20 * mm)
    widths += [20 * mm, 14 * mm, 16 * mm]

    tbl = Table(data, colWidths=widths, repeatRows=1)
    style = [
        ("BACKGROUND", (0, 0), (-1, 0), PRIMARY),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, -1), 7),
        ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#cbd5e1")),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, LIGHT]),
        ("TOPPADDING", (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 3),
    ]
    chance_col = len(headers) - 1
    for i, r in enumerate(results, start=1):
        style.append(("TEXTCOLOR", (chance_col, i), (chance_col, i), BAND_COLORS.get(r["chance"], colors.black)))
        style.append(("FONTNAME", (chance_col, i), (chance_col, i), "Helvetica-Bold"))
    tbl.setStyle(TableStyle(style))
    story.append(tbl)

    if letterhead:
        from app.services.letterhead import make_letterhead_canvas
        doc.build(story, canvasmaker=make_letterhead_canvas(letterhead))
    else:
        def _pg(canvas, doc_):
            _footer(canvas, doc_, brand_headline)

        doc.build(story, onFirstPage=_pg, onLaterPages=_pg)
    return buf.getvalue()
